class Book{
	String title, publish;
	int year, pages;

	//constructor without parameters
	public Book(){
		title = "unknow";
		publish = "yes";
		year = 2013;
		pages = 123;
	}
	//constructor with parameters
	public Book(int year){
		this();
		this.year = year;
	}
	//create a method to display info
	public void display(){
	    System.out.println("title: " + this.title);
	    System.out.println("publish? " + this.publish);
	    System.out.println("year: " + this.year);
	    System.out.println("pages: " + this.pages);
	}
	//Accessor to get title value
	public String getTitle(){
		return this.title;
	}
	//Accessor to get title publish
	public String getPublish(){
		return this.publish;
	}
	//Accessor to get title year
	public int getYear(){
		return this.year;
	}
	//Accessor to get title pages
	public int getPages(){
		return this.pages;
	}
	//Mutator to set title value
	public void setTitle(String title){
		this.title = title;
	}
	//Mutator to set publish value
	public void setPublish(String publish){
		this.publish = publish;
	}
	//Mutator to set year value
	public void setYear(int year){
		this.year = year;
	}
	//Mutator to set pages value
	public void setPages(int pages){
		this.pages = pages;
	}

}