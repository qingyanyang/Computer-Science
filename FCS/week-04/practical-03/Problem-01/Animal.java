class Animal{
	String name, favouriteFood;
	double weight;
	int age;

	//constructor without parameters
	public Animal(){
		name = "coco";
		favouriteFood = "sasuage";
		weight = 45.3d;
		age = 2;
	}
	//constructor with parameters
	public Animal(int age){
		this();
		this.age = age;
	}
	//create a method to display info
	public void display(){
		System.out.println("name: " + this.name);
		System.out.println("favourite food: " + this.favouriteFood);
		System.out.println("weight(kg): " + this.weight);
		System.out.println("age(months): " + this.age);
	}

	//Accessor to get name value
	public String getName(){
		return this.name;
	}
	//Accessor to get favouriteFood value
	public String getFavouriteFood(){
		return this.favouriteFood;
	}
	//Accessor to get weight value
	public double getWeight(){
		return this.weight;
	}
	//Accessor to get age value
	public int getAge(){
		return this.age;
	}
	//Mutator to set name value
	public void setName(String name){
		this.name = name;
	}
	//Mutator to set favouriteFood value
	public void setFavouriteFood(String favouriteFood){
		this.favouriteFood = favouriteFood;
	}
	//Mutator to set weight value
	public void setWeight(double weight){
		this.weight = weight;
	}
	//Mutator to set age value
	public void setAge(int age){
		this.age = age;
	}
}